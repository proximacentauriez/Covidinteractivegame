package com.example.multitaskgame;

import java.util.Random;

public class MathMethods {

    public int add(int int1, int int2)
    {
        int addresult = int1 + int2;

        return addresult;
    }


    public int sub(int int1, int int2)
    {
        int subresult = int1 - int2;

        return subresult;
    }

    public int mul(int int1, int int2)
    {
        int mulresult = int1 * int2;

        return mulresult;
    }
}
